import java.util.*;
import java.lang.*;
import java.io.*;

/* Name of the class has to be "Main" only if the class is public. */
class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    Scanner sc = new Scanner(System.in);
		while(sc.hasNext()) {
		    int n = sc.nextInt();
		    if (n != 42)
		        System.out.println(n);
		    else
		        break;
		}
	}
}
